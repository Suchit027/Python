tup1 = (1, 2, 3, 4, 5, 6)

# using max function
print(max(tup1))

# sorting tuples
# method 1
print(sorted(tup1, reverse=True))

# concatenating tuple
tup2 = (2, 3, 6, 4, 1)
tup3 = tup2 + tup1
print(tup3)

# deleting tuple
del tup3

# length of tuple
print(len(tup1))

# tuple multiplication
print(tup1 * 2)

# checking if element in tuple or not
print(3 in tup1)


